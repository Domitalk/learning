
// while loops 
var myArray = [];
let i = 5
while (i > -1) {
  myArray.push(i)
  i--
}

// for loops
var myArray = [];
for (let i = 1; i <= 5; i++) {
  myArray.push(i)
}

// for loop with a different incrementation 
var myArray = [];
for (let i = 1; i <= 9; i += 2) {
  myArray.push(i)
}

// increment backwards every other
var myArray = [];
for (let i = 9; i > 0; i -=2) {
  myArray.push(i)
}
